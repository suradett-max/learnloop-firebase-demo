export function buildQuestionBank() {
  const names = ["ต้นกล้า", "มิน", "ฟ้าใส", "ภูมิ", "ออมสิน", "น้ำหนึ่ง", "ธันวา", "พลอย"];
  const contexts = ["ในการอภิปราย", "ในใบงาน", "ระหว่างทำกิจกรรมกลุ่ม", "เมื่อครูให้ยกเหตุผล", "ในแบบฝึกก่อนสอบ"];
  const bank = [];
  questionSeeds.forEach((seed, seedIndex) => {
    for (let i = 0; i < 30; i += 1) {
      const rotated = rotate(seed.choices, i + seedIndex);
      const correctIndex = rotated.indexOf(seed.choices[0]);
      const prefix = contexts[(i + seedIndex) % contexts.length];
      const name = names[(i + seedIndex) % names.length];
      bank.push({
        id: `${seed.unitId}_Q${seedIndex + 1}_${i + 1}`,
        unitId: seed.unitId,
        subjectCode: units.find((item) => item.id === seed.unitId)?.subjectCode,
        difficulty: seed.difficulty,
        prompt: `${prefix} ${seed.prompt.replaceAll("ต้นกล้า", name).replaceAll("มิน", name).replaceAll("ฟ้าใส", name).replaceAll("ภูมิ", name).replaceAll("ออมสิน", name)}`,
        choices: rotated.map((text, index) => ({ key: String.fromCharCode(65 + index), text })),
        correctKey: String.fromCharCode(65 + correctIndex),
        hint: seed.hint,
        explanation: seed.explanation
      });
    }
  });
  return bank;
}

function unit(id, subjectCode, worldId, order, title, summary) {
  return { id, subjectCode, worldId, order, title, summary };
}

function q(unitId, difficulty, prompt, choices, hint) {
  return { unitId, difficulty, prompt, choices, hint, explanation: hint };
}

function rotate(items, offset) {
  const shift = offset % items.length;
  return items.slice(shift).concat(items.slice(0, shift));
}


